using ContentAggregator.Core.Entities;

namespace ContentAggregator.Core.Models
{
    public class Constants
    {
        private const string AISummaryDisclaimerGeo = "შეჯამება შედგენილია AI-ს მიერ და შეიძლება შეიცავდეს შეცდომებს.";
        private const string AISummaryDisclaimerEng = "Сводка создана ИИ и может содержать ошибки.";
        private const string AISummaryDisclaimerRus = "This summary was generated by AI and may contain errors.";

        public static string GetAiSummaryDisclaimer(SubtitleLanguage language)
        {
            return language switch
            {
                SubtitleLanguage.Georgian => AISummaryDisclaimerGeo,
                SubtitleLanguage.Russian => AISummaryDisclaimerRus,
                _ => AISummaryDisclaimerEng
            };
        }
    }
}
